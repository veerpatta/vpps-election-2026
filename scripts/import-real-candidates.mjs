import fs from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import {
  fileExists,
  mappingPath,
  readMapping,
  realCandidatesPath,
  relativeFromRoot,
  supportedPostIds,
  writeSummary,
} from './candidate-workflow.mjs'

function tsString(value) {
  return JSON.stringify(value ?? '')
}

function optionalLine(key, value) {
  return value ? `    ${key}: ${tsString(value)},\n` : ''
}

function candidateId(index, record) {
  return `candidate-${String(index + 1).padStart(3, '0')}-${record.candidateName
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')}`
}

function imageUrlFor(record) {
  if (record.finalImagePath) return record.finalImagePath
  if (record.extractedOriginalPath) return record.extractedOriginalPath
  if (record.placeholderUsed) return record.finalImagePath
  return ''
}

const postLabels = {
  'head-boy': 'Head Boy',
  'head-girl': 'Head Girl',
  'discipline-captain-boys': 'Discipline Captain Boys',
  'discipline-captain-girls': 'Discipline Captain Girls',
  'cultural-captain-boys': 'Cultural Captain Boys',
  'cultural-captain-girls': 'Cultural Captain Girls',
  'sports-captain-boys': 'Sports Captain Boys',
  'sports-captain-girls': 'Sports Captain Girls',
  'red-boys-house-captain': 'Rana Pratap Boys House Captain',
  'red-girls-house-captain': 'Rana Pratap Girls House Captain',
  'blue-boys-house-captain': 'Rana Kumbha Boys House Captain',
  'blue-girls-house-captain': 'Rana Kumbha Girls House Captain',
  'green-boys-house-captain': 'Bappa Rawal Boys House Captain',
  'green-girls-house-captain': 'Bappa Rawal Girls House Captain',
  'yellow-boys-house-captain': 'Rana Sanga Boys House Captain',
  'yellow-girls-house-captain': 'Rana Sanga Girls House Captain',
}

async function main() {
  if (!(await fileExists(mappingPath))) {
    throw new Error('Candidate mapping not found. Run npm run extract:candidate-photos first.')
  }

  const payload = await readMapping()
  const unsupported = payload.records.filter((record) => !supportedPostIds.has(record.normalizedPostId))
  if (unsupported.length > 0) {
    throw new Error(`Unsupported post ids found: ${unsupported.map((record) => `${record.rowNumber}:${record.normalizedPostId}`).join(', ')}`)
  }

  const body = payload.records.map((record, index) => {
    const imageUrl = imageUrlFor(record)
    return `  {
    id: ${tsString(candidateId(index, record))},
    name: ${tsString(record.candidateName)},
    classSection: ${tsString(record.class)},
    postId: ${tsString(record.normalizedPostId)},
    postLabel: ${tsString(postLabels[record.normalizedPostId] ?? record.postLabel)},
${optionalLine('house', record.house)}${optionalLine('captainGender', record.captainGender)}${optionalLine('category', record.category)}${optionalLine('photoUrl', imageUrl)}    approved: true,
    active: true,
  }`
  }).join(',\n')

  const content = `import type { Candidate } from '../types/election'

// Generated by scripts/import-real-candidates.mjs from data/candidate-photo-mapping.json.
// Do not edit individual candidates here; update the Excel and rerun the candidate workflow.
export const realCandidates: Candidate[] = [
${body}
]
`

  await fs.mkdir(path.dirname(realCandidatesPath), { recursive: true })
  await fs.writeFile(realCandidatesPath, content)
  await writeSummary(payload)
  console.log(`Real candidates written: ${relativeFromRoot(realCandidatesPath)}`)
  console.log(`Candidate rows imported: ${payload.records.length}`)
}

main().catch((error) => {
  console.error(error.message)
  process.exit(1)
})
